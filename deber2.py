#perímetro y área del triangulo

lado=int(input('ingrese la medida del lado del triangulo: '))
base=int(input('ingrese la medida de la base del triangulo: '))
altura=int(input('ingrese la medida de la altura del triangulo: '))

perímetro= lado * 3
área= (base * altura)/2

print('El perímetro del triangulo es:')
print (perímetro)
print('El área del triangulo es: ')
print (área)

